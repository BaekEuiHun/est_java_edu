package switch1;

public class Switch5 {
    public static void main(String[] args){

        String color = "red";
        switch (color) {
            case "red":
                System.out.println("Apple");
                break;
            case "green":
                System.out.println("Grass");
                break;
            case "blue":
                System.out.println("Sky");
                break;
            default:
        System.out.println("다시 입력하세요");
    }
    }
}
