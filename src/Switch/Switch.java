package Switch;

public class Switch {
  public static void main(String[] args) {

    int data = 10;
    switch (data) {
      case 1:
        System.out.println("1입니다");
        break;
      case 2:
        System.out.println("2입니다");
        break;
        case 10:
        System.out.println("10입니다");
        default:
        System.out.println("다시 입력하세요");
    }
  }
    }